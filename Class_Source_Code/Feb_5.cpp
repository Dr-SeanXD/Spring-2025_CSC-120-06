#include <iostream>
using namespace std;

int main() {

    cout << "Hello World" << endl;

    //Printing the double quotation.
    cout << "Hello \"Ali\"" << endl;

    //Printing the back slash.
    cout << "Printing Back Slash: \\" << endl;
    
    return 0;
}